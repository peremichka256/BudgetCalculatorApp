namespace BudgetCalculatorAppUI
{
    /// <summary>
    /// Перечисления с режимами просмотра данных
    /// </summary>
    public enum ViewMods
    {
        /// <summary>
        /// Режим в котором будут отображаться пользователи
        /// </summary>
        Users,

        /// <summary>
        /// Режим в котором будут отображаться транзакции выбранного пользователя
        /// </summary>
        Transactions,

        /// <summary>
        /// Режим в котом будет показана информация об категории транзакции
        /// </summary>
        Categories
    }
}
